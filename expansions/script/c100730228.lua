--高速决斗技能-船原雪
Duel.LoadScript("speed_duel_common.lua")
function c100730228.initial_effect(c)
	aux.SpeedDuelBeforeDraw(c,c100730228.skill)
	aux.RegisterSpeedDuelSkillCardCommon()
end
function c100730228.nttg(e,c)
	return c:IsRace(RACE_SPELLCASTER) and c:IsAttribute(ATTRIBUTE_WATER)
end
function c100730228.skill(e,tp,eg,ep,ev,re,r,rp)
	tp=e:GetLabelObject():GetOwner()
	local c=e:GetLabelObject()
	local e2=Effect.CreateEffect(c)
	e2:SetDescription(aux.Stringid(100730228,0))
	e2:SetType(EFFECT_TYPE_FIELD)
	e2:SetCode(EFFECT_EXTRA_SUMMON_COUNT)
	e2:SetTargetRange(LOCATION_HAND+LOCATION_MZONE,0)
	e2:SetTarget(c100730228.nttg)
	Duel.RegisterEffect(e2,tp)
	local e3=Effect.CreateEffect(c)
	e3:SetDescription(aux.Stringid(100730228,0))
	e3:SetType(EFFECT_TYPE_FIELD)
	e3:SetTargetRange(LOCATION_HAND,0)
	e3:SetCode(EFFECT_SUMMON_PROC)
	e3:SetTarget(c100730228.nttg)
	Duel.RegisterEffect(e3,tp)
	Duel.Hint(HINT_CARD,1-tp,100730228)
	local d=Duel.CreateToken(tp,22748199)
	Duel.SSet(tp,d)
	e:Reset()
end