--高速决斗技能-等级提升
Duel.LoadScript("speed_duel_common.lua")
function c100730281.initial_effect(c)
	aux.SpeedDuelAtMainPhase(c,c100730281.skill,c100730281.con,aux.Stringid(100730281,0))
	aux.RegisterSpeedDuelSkillCardCommon()
end
function c100730281.filter(c)
	return c:IsType(TYPE_MONSTER) and c:IsLevelAbove(1)
end
function c100730281.con(e,tp)
	tp=e:GetLabelObject():GetOwner()
	return aux.SpeedDuelAtMainPhaseCondition(e,tp)
		and Duel.IsExistingMatchingCard(c100730281.filter,tp,LOCATION_HAND,0,1,nil)
		and Duel.IsExistingMatchingCard(c100730281.filter,tp,LOCATION_MZONE,0,1,nil)
end
function c100730281.skill(e,tp)
	tp=e:GetLabelObject():GetOwner()
	Duel.Hint(HINT_CARD,1-tp,100730281)
	Duel.Hint(HINT_SELECTMSG,tp,aux.Stringid(100730281,1))
	local source=Duel.SelectMatchingCard(tp,c100730281.filter,tp,LOCATION_HAND,0,1,1,nil):GetFirst()
	Duel.ConfirmCards(1-tp,source)
	Duel.Hint(HINT_SELECTMSG,tp,aux.Stringid(100730281,2))
	local to=Duel.SelectMatchingCard(tp,c100730281.filter,tp,LOCATION_MZONE,0,1,1,nil):GetFirst()
	local e1=Effect.CreateEffect(to)
	e1:SetType(EFFECT_TYPE_SINGLE)
	e1:SetCode(EFFECT_UPDATE_LEVEL)
	e1:SetProperty(EFFECT_FLAG_CANNOT_DISABLE)
	e1:SetReset(RESET_EVENT+RESETS_STANDARD+RESET_PHASE+PHASE_END)
	e1:SetValue(source:GetLevel())
	to:RegisterEffect(e1)
	Duel.ShuffleHand(tp)
end